/// Returns a [String] in DICOM Tag Code format, e.g. (gggg,eeee),
/// corresponding to the Tag [code].
String dcm(int code) {
  assert(code >= 0 && code <= 0xFFFFFFFF, 'code: $code');
  return '(${hex16(code >> 16)},${hex16(code & 0xFFFF)})';
}

String toDcm(int code) => dcm(code);